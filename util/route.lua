function _M.reset()
	_M.set_enabled(true)
end

function _M.set_enabled(value)
	_enabled = value
end

function _M.get_best_split_frame(split)
	return _M.splits[ROUTE][split]
end

function _M.get_final_split_frame()
	return _M.splits[ROUTE]["Zeromus Death"]
end

function _M.get_value(variable)
	if not FULL_RUN or not _enabled then
		return 0
	end

	local index = _M.variables[ROUTE][variable]

	if index then
		if _M.routes[ROUTE][ENCOUNTER_SEED][index] ~= nil then
			return _M.routes[ROUTE][ENCOUNTER_SEED][index]
		else
			return 0
		end
	end

	return nil
end

